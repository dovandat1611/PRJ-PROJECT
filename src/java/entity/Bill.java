package entity;

/**
 *
 * @author Lenovo
 */
public class Bill {
    private int bill_id;
    private int order_id,user_id,product_id;
    private String product_name,image;
    private double list_price;
    private int quantity;
    private String status; 

    public Bill() {
    }

    public Bill(int bill_id, int order_id, int user_id, int product_id, String product_name, String image, double list_price, int quantity, String status) {
        this.bill_id = bill_id;
        this.order_id = order_id;
        this.user_id = user_id;
        this.product_id = product_id;
        this.product_name = product_name;
        this.image = image;
        this.list_price = list_price;
        this.quantity = quantity;
        this.status = status;
    }

    public int getBill_id() {
        return bill_id;
    }

    public void setBill_id(int bill_id) {
        this.bill_id = bill_id;
    }

    public int getOrder_id() {
        return order_id;
    }

    public void setOrder_id(int order_id) {
        this.order_id = order_id;
    }

    public int getUser_id() {
        return user_id;
    }

    public void setUser_id(int user_id) {
        this.user_id = user_id;
    }

    public int getProduct_id() {
        return product_id;
    }

    public void setProduct_id(int product_id) {
        this.product_id = product_id;
    }

    public String getProduct_name() {
        return product_name;
    }

    public void setProduct_name(String product_name) {
        this.product_name = product_name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public double getList_price() {
        return list_price;
    }

    public void setList_price(double list_price) {
        this.list_price = list_price;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

}
